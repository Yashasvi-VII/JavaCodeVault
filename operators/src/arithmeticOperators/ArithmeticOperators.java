package arithmeticOperators;

public class ArithmeticOperators {
    public static void main(String[] args) {

        int a=8;
        int b=9;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);

        double x=9;
        double y=7;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);

        // modulus operator only works in integer
        System.out.println(a%b);
    }
}
