package challenge01;

public class Patterns {
    public static void main(String[] args) {
        System.out.println("Right half pyramid");
        System.out.println("*\n*\t*\n*\t*\t*\n*\t*\t*\t*\n*\t*\t*\t*\t*");

        System.out.println();

        System.out.println("Reverse half pyramid.");
        System.out.println("*\t*\t*\t*\t*\n*\t*\t*\t*\n*\t*\t*\n*\t*\n*");

        System.out.println();

        System.out.println("Left half pyramid.");
        System.out.println("\t\t\t\t*\n\t\t\t*\t*\n\t\t*\t*\t*\n\t*\t*\t*\t*\n*\t*\t*\t*\t*");
    }
}
